function sumar(param1, param2) {
    var resultSuma = param1 + param2;
    return resultSuma;
}

function restar(param1, param2) {
    var resultResta = param1 - param2;
    return resultResta;
}

function multiplicar(param1, param2) {
    var resultMul = param1 * param2;
    return resultMul;
}

function dividir(param1, param2) {
    var resultDiv = param1 / param2;
    return resultDiv;
}